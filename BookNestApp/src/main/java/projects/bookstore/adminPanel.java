    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        String searchText = tf_search.getText().trim(); // Get the search text

        try {
            // Fetch search results from the database
            List<String[]> books = bookDetails.searchBooks(searchText);
            tableModel.setRowCount(0); // Clear the table

            // Populate the table with search results
            for (String[] book : books) {
                tableModel.addRow(new Object[]{
                    book[0], // Title
                    book[1], // Author
                    book[2], // ISBN
                    book[3], // Price
                    book[4] // Stock
                });
            }

            // Show a message if no results are found
            if (books.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No books found.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error during search: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_searchActionPerformed

