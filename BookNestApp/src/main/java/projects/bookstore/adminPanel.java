    // Load books from the database into the JTable
    private void loadBooks() {
        try {
            List<String[]> books = bookDetails.getAllBooks(); // Fetch all books
            tableModel.setRowCount(0); // Clear the existing table data
            for (String[] book : books) {
                // Add each book to the table model
                tableModel.addRow(new Object[]{
                    book[0], 
                    book[1], 
                    book[2], 
                    book[3], 
                    book[4] 
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading books: " + e.getMessage());
        }
    }

    //Loads out-of-stock books into the Out of Stock table.
    private void loadOutOfStockBooks() {
        try {
            // Fetch list of out-of-stock books using BookDetails method
            List<String[]> outOfStockBooks = bookDetails.getOutOfStockBooks();

            // Access the table model of the "Out of Stock" table to manage table data
            DefaultTableModel outOfStockTableModel = (DefaultTableModel) tbl_outofstock.getModel();
            outOfStockTableModel.setRowCount(0); // Clear any existing rows in the table

            // Add each out-of-stock book to the table model
            for (String[] book : outOfStockBooks) {
                outOfStockTableModel.addRow(new Object[]{
                    book[0], // Title
                    book[1], // Author
                    book[2] // ISBN
                });
            }
        } catch (Exception e) {
            // Display error message if any issues occur while loading out-of-stock books
            JOptionPane.showMessageDialog(this, "Error loading out-of-stock books: " + e.getMessage());
        }
    }

